function Xp = myFunbis(t,x)

% Data
c1 = 0.01; c2 = 0.1; c3 = 0.01; c4 = 0.67; c5 = 0;
M = 1; D = 1;
r1 =  -(c3 + c4*(xn(1))^2 + D*c1)/M; r2 = -D*c2*(xn(2))^2/M;
%
Z1 = x(3)^2; Z2 = x(4)^2;
M1 = (4/9)*Z1; M2 = 1 - (4/9)*Z1;
N1 = (4/9)*Z2; N2 = 1-(4/9)*Z2;
h1 = M1*N1; h2 = M1*N2; h3 = M2*N1; h4 = M2*N2;
%
A = [0 1; r1 r2]; B = [0; 1 + c5*(x(2))^2/M];
%
A1 = [0 1 ;-1.5275 -0.225];
A2 = [0 1 ;-1.5275 0];
A3 = [0 1 ;-0.02 -0.225];
A4 = [0 1 ; -0.02 0];
B = [0; 1];
%
% gain
K1 = [0.0044, -1.1769];
K2 = [0.0044, -1.1769];
K3 = [-1.5031, -0.9519];
K4 = [-1.5031, -1.1769];

%
X1 = [x(1); x(2)];
u2 = (h1*K1 + h2*K2 + h3*K3 + h4*K4)*X1;
xp1 = x(2);
xp2 = (-0.02-0.67*x(1)^2)*x(1)+0.1*x(2)^3 + u2;
Xp1 = [xp1; xp2];

X2 = [x(3); x(4)];
u2 = (h1*K1 + h2*K2 + h3*K3 + h4*K4)*X2;
Xp2 = h1*(A1*X2 + B*u2) + h2*(A2*X2 + B*u2) + h3*(A3*X2 + B*u2)+ h4*(A4*X2 + B*u2);

Xp = [Xp1; Xp2];

